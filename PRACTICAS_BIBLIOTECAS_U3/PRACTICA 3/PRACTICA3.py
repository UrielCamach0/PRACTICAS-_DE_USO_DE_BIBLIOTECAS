import math

# Valor de pi
print("Valor de pi:", math.pi)

# Valor de e (número de Euler)
print("Valor de e:", math.e)

# Raíz cuadrada
numero = 16
raiz = math.sqrt(numero)
print("Raíz cuadrada de", numero, "es:", raiz)

# Potencia
base = 2
exponente = 3
potencia = math.pow(base, exponente)
print(base, "elevado a", exponente, "es:", potencia)